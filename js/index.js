// <---------------------Age Calculator--------------------->

function change(){
    var age = document.getElementById('age').value;
    document.getElementById('month').value = (age * 12) + " Months";
    document.getElementById('day').value = (age * 12 * 30) + " Days";
    document.getElementById('hour').value = (age * 12 * 30 * 24) + " Hours";
    document.getElementById('minute').value = (age * 12 * 30 * 24 * 60) + " Minutes";
    document.getElementById('second').value = (age * 12 * 30 * 24 * 60 * 60) + " Seconds";
}

